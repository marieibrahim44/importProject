document.addEventListener("DOMContentLoaded", function() {
    const savedLanguage = localStorage.getItem("selectedLanguage") || "en";
    updateContent(savedLanguage);
});

document.querySelectorAll(".languageSelect").forEach(function(element) {
    element.addEventListener("click", function() {
        const selectedLanguage = this.getAttribute("data-lang");
        updateContent(selectedLanguage);
        localStorage.setItem("selectedLanguage", selectedLanguage);
    });
});

function updateContent(language) {
    Object.keys(translations[language]).forEach(function(key) {
        const element = document.querySelector("." + key);
        if (element) {
            // التحقق مما إذا كانت العناصر فرعية موجودة
            const subElement = element.querySelector(".auto-typed, .about-strong");
            if (subElement) {
                subElement.textContent = translations[language][key];
            } else {
                element.textContent = translations[language][key];
            }
        }
    });
    const styleSheet = language === "en" ? "assets/css/style.css" : "assets/css/style-rtl.css";
    document.getElementById("mainStyle").href = styleSheet;
}
